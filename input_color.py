def input_color():
    x = str(input("What color do you want? "))
    if x == "R":
        print("You have chosen Red.")
    elif x == "r":
        print("You have chosen Red.")
    elif x == "B":
        print("You have chosen Blue.")
    elif x == "b":
        print("You have chosen Blue.")
    elif x == "G":
        print("You have chosen Green.")
    elif x == "g":
        print("You have chosen Green.")
    else:
        print("Unknown color:", x)

input_color()



